public class Repeticao {
    public static void main(String[] args) {
        int a = 0;
        for(int i = 0; i < 5; i++){
            a++;
        }
        System.out.println(a);

        int b = 0;
        int ctd = 0;
        while(b != 5){
            b++;
            ctd++;
        }
        System.out.println(b);
        //do while -> executa primeiro, depois compara.
    }
}
